#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main() {
    int a,b,sum;
    scanf("%d",&a);
    scanf("%d",&b);
    sum = a+b;
    printf("The sum of %d and %d is %d.",a,b,sum);
    return 0;
}
